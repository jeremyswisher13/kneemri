const PEARLS_BY_REGION: Record<string, DailyPearl[]> = {
  knee: dailyPearls,
  shoulder: shoulderPearls,
  hip: hipPearls,
  elbow: elbowPearls,
};

/** The daily-pearl set for a course's body region (falls back to knee). */
export function getPearlsForRegion(bodyRegion: string): DailyPearl[] {
  return PEARLS_BY_REGION[bodyRegion] ?? dailyPearls;
}

/**
 * Returns the pearl for a given date + body region, cycling deterministically
 * through that region's list.
 */
export function getPearlForDate(bodyRegion = "knee", date: Date = new Date()): DailyPearl {
  const pearls = getPearlsForRegion(bodyRegion);
  // Compute day-of-year from LOCAL date components (midnight-to-midnight) so the
  // result is stable across the whole calendar day. Subtracting a fixed Jan-0
  // reference from date.getTime() (which includes the time-of-day) drifts by the
  // DST offset and can flip the pearl mid-day (~01:00) during DST months.
  const start = new Date(date.getFullYear(), 0, 0);
  const today = new Date(date.getFullYear(), date.getMonth(), date.getDate());
  const dayOfYear = Math.round((today.getTime() - start.getTime()) / 86400000);
  const index = ((dayOfYear % pearls.length) + pearls.length) % pearls.length;
  return pearls[index];
}
